def wrap_arabic_text(text, max_chars=60):
    if not text:
        return ""
    words = text.split()
    lines = []
    current_line = []
    current_length = 0
    for word in words:
        if current_length + len(word) + 1 > max_chars:
            lines.append(" ".join(current_line))
            current_line = [word]
            current_length = len(word)
        else:
            current_line.append(word)
            current_length += len(word) + 1
    if current_line:
        lines.append(" ".join(current_line))
        
    reshaped_lines = []
    for line in lines:
        reshaped = arabic_reshaper.reshape(line)
        bidi_line = get_display(reshaped)
        reshaped_lines.append(bidi_line)
    return "<br/>".join(reshaped_lines)

def build_category_table(data, col_widths, header_style, cell_style, formula_style):
    """دالة مساعدة لبناء جداول المؤشرات"""
    table_data = [
        [Paragraph(wrap_arabic_text("المؤشر الانتخابي", 25), header_style), 
         Paragraph(wrap_arabic_text("المعادلة الرياضية وآلية الحساب التفصيلية", 30), header_style), 
         Paragraph(wrap_arabic_text("التفسير والأهمية الاستراتيجية للفوز", 40), header_style)]
    ]
    for row in data:
        table_data.append([
            Paragraph(wrap_arabic_text(row[0], 25), cell_style),
            Paragraph(wrap_arabic_text(row[1], 35), formula_style),
            Paragraph(wrap_arabic_text(row[2], 40), cell_style)
        ])
        
    t = Table(table_data, colWidths=col_widths)
    t.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, 0), colors.HexColor('#0f3b7d')),
        ('ALIGN', (0, 0), (-1, -1), 'RIGHT'),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ('GRID', (0, 0), (-1, -1), 0.5, colors.HexColor('#cbd5e1')),
        ('TOPPADDING', (0, 0), (-1, -1), 6),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 6),
    ]))
    return t

